package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; // 불리언
        char d = 'A'; // 문자 하나! 두 개는 안 됨, 홑따옴표 또는 작은따옴표
        String e = "Hello Java"; // 문자열을 다루는 클래스, 얘만 특별하게 대문자로 시작, 쌍따옴표 또는 큰따옴표

        // int z = "100";

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e); // 윈도우는 Ctrl+D로 한 줄 복사 가능(Duplicate Line)

    }
}
