package variable;

public class Var4 {
    public static void main(String[] args) {
        int a, b, c, d;

        a = 1;
        System.out.println(a);

        b = 2; // 선언과 동시에 초기화
        System.out.println(b);

        int f = 3, g = 4; // 여러 변수 선언과 동시에 초기화
        System.out.println(f);
        System.out.println(g);
    }
}
